/*

Operator precedence describes the order in which operations are performed in an arithmetic expression.

Multiplication (*) and division (/) have higher precedence than addition (+) and subtraction (-).

*/

let x = 100 + 50 * 3; //250


let y = (100 + 50) * 3; //450
 

let z = 100 / 50 * 3; //6

console.log(x,y,z);
